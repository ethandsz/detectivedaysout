showAlertDialog(BuildContext context, markerInfo.ClueLocation marker) {
  // set up the button
  Widget okButton = ButtonTheme(
      child: Align(
          alignment: Alignment.center,
          child: TextButton(
            child: Text("Continue"),
            style: TextButton.styleFrom(
                primary: Colors.black,
                backgroundColor: Colors.white,
                textStyle: const TextStyle(
                    fontFamily: 'Akrobat',
                    fontSize: 18,
                    color: Colors.black,
                    fontWeight: FontWeight.bold)),
            onPressed: () {
              marker.compl = true;
              updateMarker = true;
              Navigator.of(context).pop();
            },
          )));

  // set up the AlertDialog
  AlertDialog alert = AlertDialog(
    title: Text(marker.title),
    content: Column(children: <Widget>[
      if (marker.body != null) Text(marker.body),
      if (marker.videoLink != null)
        vid.VideoItem(
            videoPlayerController:
                VideoPlayerController.network(marker.videoLink),
            looping: false)
    ]),
    actions: [
      okButton,
    ],
    scrollable: true,
    backgroundColor: Colors.white,
    contentTextStyle: TextStyle(
        fontFamily: 'Akrobat',
        fontSize: 18,
        color: Colors.black,
        fontWeight: FontWeight.bold),
    shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(25)),
    titleTextStyle: TextStyle(
        fontSize: 18,
        color: Colors.black,
        fontWeight: FontWeight.w900,
        fontFamily: 'Akrobat'),
  );

  // show the dialog
  showDialog(
    context: context,
    builder: (BuildContext context) {
      return alert;
    },
  );
}

